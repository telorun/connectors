#!/usr/bin/env node
// Generates `.changie.yaml` and bootstraps the `.changes/` version ledger from the connector
// modules. changie owns each module's `metadata.version` (the language-agnostic module version
// published to the OCI registry); changesets stays the owner of the npm controller packages.
//
// One changie "project" per connector module under <vendor>/<module>/ that declares a
// metadata.version (e.g. aws/s3, aws/lambda, google/sheets). Each project's `replacements` rule
// writes the bumped version back into its telo.yaml. `.changes/<key>/<version>.md` files are the
// version ledger — seeded here from the current metadata.version so the first batch continues
// from today's version instead of 0.0.0.
//
// Re-run after adding/removing a module. CI checks the result is committed (no drift).
//
// Usage: node scripts/gen-changie-config.mjs

import { existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const CHANGES_DIR = join(ROOT, ".changes");

// Top-level directories that are never vendor namespaces.
const SKIP_DIRS = new Set([
  ".git",
  ".github",
  ".changes",
  ".changeset",
  ".telo",
  "node_modules",
  "scripts",
  "dist",
]);

/** Parse the first YAML document's metadata block for name/version + quote style. */
function readModuleMeta(manifestPath) {
  const content = readFileSync(manifestPath, "utf8");
  const docEnd = content.search(/^---\s*$/m);
  const firstDoc = docEnd === -1 ? content : content.slice(0, docEnd);
  const metaMatch = firstDoc.match(/^metadata:\s*\n((?:[ \t]+.*\n?)+)/m);
  if (!metaMatch) return null;
  const block = metaMatch[1];
  const name = block.match(/^[ \t]+name:[ \t]*["']?([^\s"']+)/m)?.[1];
  const versionLine = block.match(/^[ \t]+version:[ \t]*(["']?)(\d+\.\d+\.\d+)\1[ \t]*$/m);
  if (!name || !versionLine) return null;
  return { name, version: versionLine[2], quoted: versionLine[1] !== "" };
}

// A changie project is any <vendor>/<module>/ that ships a telo.yaml with a metadata.version, so
// onboarding a new connector needs no edit here, just the manifest. The project `key` is the
// module directory name.
function scanModules() {
  const out = [];
  for (const vendor of readdirSync(ROOT, { withFileTypes: true })) {
    if (!vendor.isDirectory() || SKIP_DIRS.has(vendor.name)) continue;
    const vendorDir = join(ROOT, vendor.name);
    for (const mod of readdirSync(vendorDir, { withFileTypes: true })) {
      if (!mod.isDirectory()) continue;
      const manifest = join(vendorDir, mod.name, "telo.yaml");
      if (!existsSync(manifest)) continue;
      out.push({ key: mod.name, dirRel: `${vendor.name}/${mod.name}`, manifest });
    }
  }
  return out;
}

const projectDefs = scanModules()
  .map((m) => ({ ...m, meta: readModuleMeta(m.manifest) }))
  .filter((m) => m.meta) // skip manifests without a metadata.version
  .sort((a, b) => a.key.localeCompare(b.key));

// The changie replacement rewrites the 2-space semver `version:` line with a file-global regexp
// ReplaceAll. A manifest must contain exactly one such line — an unexpected extra one (e.g. a
// Telo.Definition field named version) would be silently overwritten on release. Enforce it here
// (CI runs this generator as a drift check) so a stray semver version field is a build error.
for (const m of projectDefs) {
  const matches = (
    readFileSync(m.manifest, "utf8").match(/^ {2}version: ["']?\d+\.\d+\.\d+["']?$/gm) ?? []
  ).length;
  if (matches !== 1) {
    throw new Error(
      `${m.manifest}: expected exactly 1 two-space-indented semver \`version:\` line for the ` +
        `changie replacement, found ${matches}. An unexpected one would be corrupted on release ` +
        `— move or re-indent it.`,
    );
  }
}

// --- .changie.yaml ---------------------------------------------------------

const head = `# GENERATED by scripts/gen-changie-config.mjs — do not edit by hand.
# Re-run \`node scripts/gen-changie-config.mjs\` after adding or removing a module.
changesDir: .changes
unreleasedDir: unreleased
versionExt: md
headerPath: header.tpl.md
versionFormat: '## {{.Version}} - {{.Time.Format "2006-01-02"}}'
kindFormat: '### {{.Kind}}'
changeFormat: '* {{.Body}}'
kinds:
  - label: Added
    auto: minor
  - label: Changed
    auto: major
  - label: Deprecated
    auto: minor
  - label: Removed
    auto: major
  - label: Fixed
    auto: patch
  - label: Security
    auto: patch
projects:
`;

const projects = projectDefs
  .map((m) => {
    const replaceVal = m.meta.quoted
      ? `  version: "{{.VersionNoPrefix}}"`
      : `  version: {{.VersionNoPrefix}}`;
    return [
      `  - label: ${m.key}`,
      `    key: ${m.key}`,
      `    changelog: ${m.dirRel}/CHANGELOG.md`,
      `    replacements:`,
      `      - path: ${m.dirRel}/telo.yaml`,
      `        find: '^  version: ["'']?\\d+\\.\\d+\\.\\d+["'']?$'`,
      `        replace: '${replaceVal}'`,
    ].join("\n");
  })
  .join("\n");

writeFileSync(join(ROOT, ".changie.yaml"), head + projects + "\n", "utf8");

// --- ledger bootstrap ------------------------------------------------------

mkdirSync(join(CHANGES_DIR, "unreleased"), { recursive: true });
const gitkeep = join(CHANGES_DIR, "unreleased", ".gitkeep");
if (!existsSync(gitkeep)) writeFileSync(gitkeep, "", "utf8");

const headerPath = join(CHANGES_DIR, "header.tpl.md");
if (!existsSync(headerPath)) writeFileSync(headerPath, "# Changelog\n", "utf8");

let seeded = 0;
for (const m of projectDefs) {
  const projectDir = join(CHANGES_DIR, m.key);
  mkdirSync(projectDir, { recursive: true });
  const hasVersionFile = readdirSync(projectDir).some((f) => /^\d+\.\d+\.\d+\.md$/.test(f));
  if (hasVersionFile) continue;
  writeFileSync(join(projectDir, `${m.meta.version}.md`), `## ${m.meta.version}\n`, "utf8");
  seeded++;
}

console.log(
  `gen-changie-config: ${projectDefs.length} project(s) written to .changie.yaml, ${seeded} ledger version(s) seeded.`,
);
